/**
 * Universal Chart of Accounts Seed Data
 *
 * Default COA for ALL Oppsera tenant business types.
 * Seeded on tenant creation — every field is soft-coded and editable by the tenant.
 * Tenants can rename, re-number, deactivate, or add accounts after seeding.
 *
 * Numbering scheme (5-digit):
 *   10000–14999  Current Assets
 *   15000–15999  Fixed Assets
 *   16000–16999  Intangible Assets
 *   20000–23999  Current Liabilities
 *   24000–25999  Long-Term Liabilities
 *   30000–33999  Equity
 *   40000–43999  Revenue
 *   49000–49999  Contra Revenue
 *   50000–51999  Cost of Goods Sold
 *   60000–69999  Operating Expenses
 *   70000–79999  Reserved (future expense categories)
 *   80000–82999  Other Income / Expense
 *   90000–99999  Statistical / System
 *
 * Integration note — existing Oppsera schema mapping:
 *   Seed type    →  gl_accounts.account_type
 *   ASSET        →  'asset'
 *   LIABILITY    →  'liability'
 *   EQUITY       →  'equity'
 *   REVENUE      →  'revenue'
 *   COGS         →  'expense'   (classification: "Cost of Goods Sold")
 *   EXPENSE      →  'expense'
 *   OTHER_INCOME →  'revenue'   (classification: "Other Income")
 *   OTHER_EXPENSE→  'expense'   (classification: "Other Expense")
 */


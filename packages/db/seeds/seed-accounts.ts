// ── Validation helpers ──────────────────────────────────────────────

/** Verify parent-child integrity: every parentAccountNumber must exist in the dataset */
export function validateParentIntegrity(accounts: SeedAccount[]): string[] {
  const numbers = new Set(accounts.map((a) => a.accountNumber));
  const errors: string[] = [];
  for (const acct of accounts) {
    if (acct.parentAccountNumber && !numbers.has(acct.parentAccountNumber)) {
      errors.push(
        `Account ${acct.accountNumber} "${acct.name}" references parent ${acct.parentAccountNumber} which does not exist`,
      );
    }
  }
  return errors;
}

/** Verify no duplicate account numbers */
export function validateUniqueNumbers(accounts: SeedAccount[]): string[] {
  const seen = new Map<string, string>();
  const errors: string[] = [];
  for (const acct of accounts) {
    if (seen.has(acct.accountNumber)) {
      errors.push(
        `Duplicate account number ${acct.accountNumber}: "${seen.get(acct.accountNumber)}" and "${acct.name}"`,
      );
    }
    seen.set(acct.accountNumber, acct.name);
  }
  return errors;
}

/** Verify no duplicate account roles */
export function validateUniqueRoles(accounts: SeedAccount[]): string[] {
  const seen = new Map<string, string>();
  const errors: string[] = [];
  for (const acct of accounts) {
    if (acct.accountRole) {
      if (seen.has(acct.accountRole)) {
        errors.push(
          `Duplicate role ${acct.accountRole}: "${seen.get(acct.accountRole)}" and "${acct.name}"`,
        );
      }
      seen.set(acct.accountRole, acct.name);
    }
  }
  return errors;
}

/** Topologically sort accounts so parents are inserted before children */
export function topologicalSort(accounts: SeedAccount[]): SeedAccount[] {
  const byNumber = new Map(accounts.map((a) => [a.accountNumber, a]));
  const sorted: SeedAccount[] = [];
  const visited = new Set<string>();

  function visit(acct: SeedAccount) {
    if (visited.has(acct.accountNumber)) return;
    if (acct.parentAccountNumber) {
      const parent = byNumber.get(acct.parentAccountNumber);
      if (parent) visit(parent);
    }
    visited.add(acct.accountNumber);
    sorted.push(acct);
  }

  for (const acct of accounts) {
    visit(acct);
  }
  return sorted;
}
